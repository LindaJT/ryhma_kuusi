

public class Bookmark {
    
    protected String title;

    public Bookmark() {
    }

    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param newTitle the title to set
     */
    public void setTitle(final String newTitle) {
        this.title = newTitle;
    }
    
}
